class intQueue {   // очередь целых чисел
    int[] queue;  // массив типа int для хранения элементов очереди
    int putloc, getloc;

    intQueue(int size){         // Конструктор класса Queue создает очередь заданного размера.
        queue = new int[size]; //выделение памяти для очереди
        putloc = getloc = 0;
    }

    // Помещение в очередь
    void put(int i) {
        if (putloc == queue.length) {
            System.out.println(" - Очередь заполнена");
            return;
        }
        queue[putloc++] = i;
    }

    // Извлечение из очереди
    int get() {
        if(getloc == putloc) {
            System.out.println(" - Очередь пуста");
            return (int) 0;
        }
        return queue[getloc++];
    }
}
